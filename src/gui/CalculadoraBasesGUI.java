    private void jButtonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualsActionPerformed
        // TODO add your handling code here:
        char last =jTextFieldDisplay.getText().charAt(jTextFieldDisplay.getText().length()-1);
        String cadena = jTextFieldDisplay.getText();
        int posicion =0;
        if(cadena.indexOf('+') != -1){
            posicion = cadena.indexOf('+')+1;
        }
        if(cadena.substring(1).indexOf('-') != -1){
            posicion = cadena.indexOf('-')+1;
        }
        if(cadena.indexOf('*') != -1){
            posicion = cadena.indexOf('*')+1;
        }
        if(last!='+'&&last!='-'&&last!='*'){
            if(base<=10){
                operacion.setOperando2(Integer.parseInt(cadena.substring(posicion)));
            }else if(base>10){
                operacion.setOperando2(cadena.substring(posicion));
            }
            operacion.realizarOperacion();
            operacion.setOperando1(operacion.getResultado());
            jTextFieldDisplay.setText(String.valueOf(operacion.getResultado()));
            estado = false;            
        }
    }//GEN-LAST:event_jButtonEqualsActionPerformed

