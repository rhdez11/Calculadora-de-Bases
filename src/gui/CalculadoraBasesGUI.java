    private void actionPerformedOperador(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionPerformedOperador
        // TODO add your handling code here:
        javax.swing.JButton jb = (javax.swing.JButton)evt.getSource();
        String cadena = jTextFieldDisplay.getText();
        int posicion =0;
        if(cadena.indexOf('+') != -1){
            posicion = cadena.indexOf('+')+1;
        }
        if(cadena.substring(1).indexOf('-') != -1){
            posicion = cadena.indexOf('-')+1;
        }
        if(cadena.indexOf('*') != -1){
            posicion = cadena.indexOf('*')+1;
        }
        if(!estado&&base<=10){
            estado = true;
            operacion.setOperando1(Integer.parseInt(cadena));
            jTextFieldDisplay.setText(cadena+jb.getText());
            operacion.setOperador(jb.getText().charAt(0));
        }else if(estado&&base<=10){            
            operacion.setOperando2(Integer.parseInt(cadena.substring(posicion)));
            operacion.realizarOperacion();
            jTextFieldDisplay.setText(String.valueOf(operacion.getResultado())+jb.getText());
            operacion.setOperando1(operacion.getResultado());
            operacion.setOperador(jb.getText().charAt(0));
        }else if(!estado&&base>10){
            estado = true;
            operacion.setOperando1(cadena);
            jTextFieldDisplay.setText(cadena+jb.getText());
            operacion.setOperador(jb.getText().charAt(0));
        }else if(estado&&base>10){
            operacion.setOperando2(cadena.substring(posicion));
            operacion.realizarOperacion();
            jTextFieldDisplay.setText(operacion.getResultado()+jb.getText());
            operacion.setOperando1(operacion.getResultado());
            operacion.setOperador(jb.getText().charAt(0));
        }        
    }//GEN-LAST:event_actionPerformedOperador

